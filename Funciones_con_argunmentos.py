#Un parámetro se define como una variable definida entre los parentesis de la definición de la función
#Un argumento (arg) es el valor enviado a la función

def funcion_arg(nombre, apellido):
    print("El nombre recibido es:", nombre)
    print("El apellido recibido es:", apellido)
    
funcion_arg("Juan", "Perez") #Esto envía un parámetro a la función para ser ejecutada
#El argumento se envía al parámetro de la función, valor que le asignamos a nuestra variable

funcion_arg("Karla", "Gomez") #Se debe otorgar todos los argumentos que todos los parámetros esperan
